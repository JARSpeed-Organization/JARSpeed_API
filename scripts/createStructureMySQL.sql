USE `jarspeed`;

CREATE TABLE `SEX` (
  `ID` int(11) NOT NULL,
  `LABEL` varchar(255) NOT NULL
);

CREATE TABLE `USER` (
  `LASTNAME` varchar(255) NOT NULL,
  `FIRSTNAME` varchar(255) NOT NULL,
  `EMAIL` varchar(255) NOT NULL,
  `AGE` int(11) NOT NULL,
  `SEX` int(11) NOT NULL,
  `WEIGHT` double NOT NULL,
  `PASSWORD` varchar(255) NOT NULL
);

ALTER TABLE `SEX`
  ADD PRIMARY KEY (`ID`);

ALTER TABLE `USER`
  ADD PRIMARY KEY (`EMAIL`),
  ADD KEY `SEX` (`SEX`);

