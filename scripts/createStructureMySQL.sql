  MODIFY `ID` int(11) NOT NULL AUTO_INCREMENT;

ALTER TABLE `USER`
  ADD CONSTRAINT `USER_ibfk_1` FOREIGN KEY (`SEX`) REFERENCES `SEX` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION;
COMMIT;


